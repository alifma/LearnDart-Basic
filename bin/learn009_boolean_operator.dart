/// Operator perbandingan pada dart
void main() {
  final a = 1;
  final b = 3;

  dynamic output;

  /// ==
  output = a == b;
  print('Output dari $a == $b adalah $output');

  /// !=
  output = a != b;
  print('Output dari $a != $b adalah $output');

  /// >
  output = a > b;
  print('Output dari $a > $b adalah $output');

  /// <
  output = a < b;
  print('Output dari $a < $b adalah $output');

  /// <=
  output = a <= b;
  print('Output dari $a <= $b adalah $output');

  /// >=
  output = a >= b;
  print('Output dari $a >= $b adalah $output');
}
